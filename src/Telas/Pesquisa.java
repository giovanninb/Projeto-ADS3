        public void excluirContato() throws SQLException {
        if (!TXTCodigo.equals("")) {
            int resp = JOptionPane.showConfirmDialog(this, "deseja realmente excluir ?", "Confirmação", JOptionPane.YES_NO_OPTION);
            if (resp == JOptionPane.YES_NO_OPTION) {
                PesquisaDao dao = new PesquisaDao();
                dao.remove(Ti.get(jTabela.getSelectedRow()));
                mostraPesquisa(Ti);
            }
            mostraPesquisa(Ti);
        }else{
            JOptionPane.showMessageDialog(null, "Não tem ID no Campo");
        }
    }
    
