            private void Editar() throws SQLException {
                try {
            PesquisaDTO dto = new PesquisaDTO();
            dto.setCodigo(Integer.parseInt(TXTCodigo.getText()));
            dto.setNome(TXTNome.getText());
            if(TXTStatus.getSelectedIndex() == 1){
             dto.setStatus("Ativo");   
            }else if(TXTStatus.getSelectedIndex() == 2){
             dto.setStatus("Desativo");   
            }
            String valor = TXTValor.getText().replace(",", ".");
            dto.setValor(Double.parseDouble(valor));
            PesquisaDao dao = new PesquisaDao();
            dao.update(dto);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Cadostro" + ex);
        }
            listarContatos();
    }
    
